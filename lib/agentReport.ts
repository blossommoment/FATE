// Agent 报告管线（OKX 黑客松：买方 agent 经 Hermes 店员下单，本模块是后厨）。
// 任务制：POST 建任务立即返回，轮询取货；同一对盘缓存成品，不重复烧 AI。
// 双语范围（用户拍板）：目录与免责声明走固定对照表，执行摘要走一次翻译调用；章节正文中文。

export type AgentReportInput = { a: BirthInput; b: BirthInput; relationType: string; bilingual: boolean };

export type AgentReportJson = {
  reportId: string;
  generatedAt: string;
  brand: string;
  relationType: string;
  persons: DuoFacts["persons"];
  score: number;
  verdict: RelationshipAnalysis["guide"]["verdict"];
  scoreBreakdown: RelationshipAnalysis["scoreBreakdown"];
  toc: { no: string; zh: string; en: string }[];
  summary: { zh: string; en?: string };
  chapters: {
    overview: { thesis: string; scoreSummary: string };
    nature: { manuals: RelationshipAnalysis["guide"]["manuals"]; diffs: { label: string; a: number; b: number; aReading: string; bReading: string }[] };
    structure: RelationshipAnalysis["branchDynamics"];
    manner: RelationshipAnalysis["guide"]["behaviors"];
    reef: { philosophy: string; hotspots: RelationshipAnalysis["guide"]["hotspots"] };
    rhythm: DuoYearOutlook[];
  };
  book: { source: "ai" | "fallback"; headline: string; pages: Record<string, { essay: string; advice: string }> };
  disclaimer: { zh: string; en: string };
};

export type AgentReportJob = {
  id: string;
  status: "processing" | "done" | "failed";
  step: string;
  error?: string;
  createdAt: number;
  bilingual: boolean;
  result?: { reportId: string; pdfPath: string; report: AgentReportJson };
};

